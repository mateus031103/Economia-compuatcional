# Declaração de variáveis
numero_1 = 0
numero_2 = 0
soma = 0

# entrada de Dados
numero_1 = int(input("\nDigite o primeiro número"))
numero_2 = int(input("\nDigite o segundo número"))

# Processamento de Dados
soma = numero_1 + numero_2

# Saída de dados
print(f'A soma dos números é igual a {soma}')